    private void setTextFields(){
        //TEXTNAME
        testTitleTextField.setText(testName);
        //DESCRIPTIONS
        qDescTextField.setText(qTextArray[currentQuestion]);
        //TRUE
        trueCode0.setText(trueAnswers[currentQuestion][0]);
        trueCode1.setText(trueAnswers[currentQuestion][1]);
        trueCode2.setText(trueAnswers[currentQuestion][2]);
        //FALSE
        falseCode0.setText(falseAnswers[currentQuestion][0]);
        falseCode1.setText(falseAnswers[currentQuestion][1]);
        falseCode2.setText(falseAnswers[currentQuestion][2]);
        falseCode3.setText(falseAnswers[currentQuestion][3]);
        falseCode4.setText(falseAnswers[currentQuestion][4]);
        falseCode5.setText(falseAnswers[currentQuestion][5]);
    }
    
    private void initDefaultTestValues(){
        for(int i = 0; i < NO_OF_QUESTIONS; i++){
            qTextArray[i] = "<html>default Description: enter some new text here";
        }
        for(int i = 0; i < NO_OF_QUESTIONS; i++){
            for(int j = 0; j < 3; j++){
                trueAnswers[i][j] = "True code snippet " + (j+1) +"/3";
            }
        }
        for(int i = 0; i < NO_OF_QUESTIONS; i++){
            for(int j = 0; j < 6; j++){
                falseAnswers[i][j] = "False code snippet " + (j+1) +"/6";
            }
        }
    }

