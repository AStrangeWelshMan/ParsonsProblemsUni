// Previous/next buttons
    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        //Clear the feedback message
        feedbackText.setText("");
        if(currentQuestion == 9){
            feedbackText.setText("There are no more questions");
        } else {
            //TESTNAME
            testName = testTitleTextField.getText();
            //DESCRIPTION
            qTextArray[currentQuestion] = qDescTextField.getText();
            //TRUE
            trueAnswers[currentQuestion][0] = trueCode0.getText();
            trueAnswers[currentQuestion][1] = trueCode1.getText();
            trueAnswers[currentQuestion][2] = trueCode2.getText();
            //FALSE
            falseAnswers[currentQuestion][0] = falseCode0.getText();
            falseAnswers[currentQuestion][1] = falseCode1.getText();
            falseAnswers[currentQuestion][2] = falseCode2.getText();
            falseAnswers[currentQuestion][3] = falseCode3.getText();
            falseAnswers[currentQuestion][4] = falseCode4.getText();
            falseAnswers[currentQuestion][5] = falseCode5.getText();
            //increase question number and set new values in the text fields
            currentQuestion++;
            setTextFields();
        }
    }//GEN-LAST:event_nextButtonActionPerformed

    private void previousButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previousButtonActionPerformed
        //Clear the feedback message
        feedbackText.setText("");
        if(currentQuestion == 0){
            feedbackText.setText("");
        } else {
            //TESTNAME
            testName = testTitleTextField.getText();
            //DESCRIPTION
            qTextArray[currentQuestion] = qDescTextField.getText();
            //TRUE
            trueAnswers[currentQuestion][0] = trueCode0.getText();
            trueAnswers[currentQuestion][1] = trueCode1.getText();
            trueAnswers[currentQuestion][2] = trueCode2.getText();
            //FALSE
            falseAnswers[currentQuestion][0] = falseCode0.getText();
            falseAnswers[currentQuestion][1] = falseCode1.getText();
            falseAnswers[currentQuestion][2] = falseCode2.getText();
            falseAnswers[currentQuestion][3] = falseCode3.getText();
            falseAnswers[currentQuestion][4] = falseCode4.getText();
            falseAnswers[currentQuestion][5] = falseCode5.getText();
            //decrease question number and set new values in the text fields
            currentQuestion--;
            setTextFields();
        }
    }//GEN-LAST:event_previousButtonActionPerformed

