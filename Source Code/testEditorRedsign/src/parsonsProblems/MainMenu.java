    // If the necessary directories do not exist, then create them
    private void createDirectories(){
        //System.out.println("Checking folder structure");
        String testsString = "TESTDATA" + File.separator + "Tests";
        File tests = new File(testsString);
        if(tests.exists() != true){
            System.out.println("Folder \"tests\" does not exist!\nAttempting to create folder...");
            if(tests.mkdirs()){
                System.out.println("Tests Folder creation successful!");
            }
        }
        else{
            System.out.println("Tests Folder already exists, skipping");
        }
        String tempString = "TESTDATA" + File.separator + "Temp";
        File temp = new File(tempString);
        if(temp.exists() != true){
            System.out.println("Folder \"Temp\" does not exist!\nAttempting to create folder...");
            if(temp.mkdirs()){
                System.out.println("Temp Folder creation successful!");
            }
        }
        else{
            System.out.println("Temp Folder already exists, skipping");
        }
        String sessString = "TESTDATA" + File.separator + "Sessions";
        File sess = new File(sessString);
        if(sess.exists() != true){
            System.out.println("Folder \"Sessions\" does not exist!\nAttempting to create folder...");
            if(sess.mkdirs()){
                System.out.println("Sessions Folder creation successful!");
            }
        }
        else{
            System.out.println("Session folder already exists, skipping");
        }
        String resString = "TESTDATA" + File.separator + "Results";
        File res = new File(resString);
        if(res.exists() != true){
            System.out.println("Folder \"Results\" does not exist!\nAttempting to create folder...");
            if(res.mkdirs()){
                System.out.println("Results Folder creation successful!");
            }
        }
        else{
            System.out.println("Results folder already exists, skipping");
        }
    }

